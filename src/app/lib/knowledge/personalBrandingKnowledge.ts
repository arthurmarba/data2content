/**
 * Retorna princípios chave para construir uma marca pessoal forte (Didático e Guiado).
 */
export function getPersonalBrandingPrinciples(): string {
    // Ano atual para contexto
    const currentYear = new Date().getFullYear();
    return `
**Os Pilares da Sua Marca Pessoal (${currentYear}): Como Construir uma Base Forte?**

Construir sua marca pessoal é como construir uma casa: precisa de uma base sólida. Aqui estão os pilares essenciais:

* **Autenticidade (Ser Você Mesmo):** A base de tudo! As pessoas (e as marcas!) valorizam quem é **genuíno e transparente**. Não tente ser alguém que você não é. Mostre um pouco dos seus bastidores, interaja de verdade. Isso cria **confiança e conexão**, que são super valiosos.
    * *Por que é importante?* Porque as pessoas se conectam com pessoas, não com personagens perfeitos. A autenticidade te diferencia e atrai quem realmente se identifica com você.

* **Nicho e Autoridade (Sua Área de Expertise):** Defina uma área ou assunto que você **ama e entende bem** (seu nicho). Ao compartilhar consistentemente conteúdo de valor sobre esse tema, você se torna uma **referência** (autoridade) nele.
    * *Qual a vantagem?* Isso atrai as pessoas certas (seu público ideal) e também marcas que buscam alguém com seu conhecimento específico.

* **Valores e Propósito (O Que Te Move):** Quais são as coisas em que você acredita? Comunicar seus **valores** cria uma conexão mais profunda com seguidores que pensam parecido e atrai marcas que compartilham das mesmas ideias. Seus valores devem guiar o tipo de conteúdo que você faz e as parcerias que aceita.
    * *Como isso ajuda?* Define quem você é além do conteúdo, criando uma comunidade mais engajada e fiel.

* **Voz da Marca (Seu Jeito de Falar):** Desenvolva um **tom e estilo de comunicação** que seja só seu e que combine com sua personalidade e valores. Você é mais informal? Divertido? Inspirador? Sério? Mantenha essa "voz" em tudo que você faz online para que as pessoas te reconheçam facilmente.
    * *Por que ter uma voz única?* Ajuda a criar reconhecimento e a tornar sua comunicação mais marcante e pessoal.

*Resumindo: Ser autêntico + Focar no seu nicho + Ter valores claros + Comunicar com sua própria voz = Uma marca pessoal forte e que se destaca.*

**Qual desses pilares você sente que já trabalha bem? E qual gostaria de desenvolver mais? Podemos focar em como definir melhor seu nicho ou encontrar sua voz única, que tal?**
  `;
}

/**
 * Explica a importância da estética, identidade visual e storytelling visual (Didático e Guiado).
 */
export function explainAestheticsAndVisualStorytelling(): string {
    return `
**Aparência Importa? Sim! Estética e Como Contar Histórias com Imagens**

Assim como seu jeito de falar (sua voz), a **aparência** do seu conteúdo (sua estética) também comunica muito sobre sua marca pessoal. Vamos ver como cuidar disso:

* **Identidade Visual (Sua "Cara" Online):** Pense nos elementos visuais que vão representar sua marca. É como escolher a "roupa" do seu perfil:
    * **Paleta de Cores:** Escolha algumas cores que combinem com você, seu nicho e seus valores (ex: tons claros para algo delicado, cores vibrantes para energia, tons terrosos para algo natural). Use essas cores sempre!
    * **Fontes (Tipografia):** As letras que você usa também passam uma mensagem (uma fonte mais desenhada pode parecer elegante, uma mais grossa pode parecer moderna). Escolha 1 ou 2 fontes e use-as de forma consistente.
    * **Estilo de Edição:** Defina um padrão para editar suas fotos e vídeos (ex: um filtro específico, um estilo mais "clean", mais "vintage", etc.). Isso cria unidade.

* **Feed Organizado (Seu Cartão de Visitas no Instagram):** Um feed visualmente agradável passa profissionalismo. Não precisa ser perfeito, mas ter uma harmonia ajuda. Pense em como as fotos conversam entre si. Usar um mesmo filtro ou padrão de cores ajuda muito. Existem até apps para planejar como seu feed vai ficar antes de postar.

* **Contando Histórias com Imagens (Storytelling Visual):** Use suas fotos e vídeos para contar histórias e reforçar sua mensagem. Um carrossel pode dividir uma ideia em partes, uma boa foto de capa (thumbnail) no YouTube chama atenção, os Stories mostram os bastidores do seu dia. A estética deve ajudar a contar quem você é.

* **Consistência é a Chave:** O mais importante é manter uma **coerência visual** em tudo que você faz (posts, stories, outras redes). Isso faz com que as pessoas te reconheçam na hora e passa uma imagem mais profissional.

*Resumindo: Uma identidade visual bem definida e consistente ajuda as pessoas a te reconhecerem, passa profissionalismo e reforça a mensagem da sua marca pessoal.*

**Você já pensou sobre sua identidade visual? Quais cores e estilos combinam mais com você e seu conteúdo? Quer ajuda para definir uma paleta de cores ou pensar em como organizar melhor seu feed?**
  `;
}

/**
 * Detalha as diferenças de posicionamento e estratégias recomendadas por porte do criador (Didático e Guiado).
 */
export function explainPositioningBySize(): string {
    return `
**Sua Estratégia Muda Conforme Você Cresce: Posicionamento por Tamanho**

A forma como você se posiciona e a estratégia da sua marca pessoal podem (e devem!) se adaptar conforme sua audiência cresce. Vamos ver as fases:

* **Nano Influenciador (até uns 10 mil seguidores):**
    * **Sua Força:** Engajamento nas alturas! Você conhece seu público, fala de um nicho bem específico, é visto como alguém "real", próximo. As pessoas confiam muito em você.
    * **Estratégia:** Mergulhe fundo no seu nicho! Crie uma comunidade bem unida, responda todo mundo, mostre os bastidores. Sua autenticidade é seu superpoder. Busque parcerias que tenham tudo a ver com você (mesmo que sejam trocas no início) para construir seu portfólio.

* **Micro Influenciador (10 mil a 100 mil seguidores):**
    * **Sua Força:** O ponto de equilíbrio perfeito! Você tem bom alcance, mas ainda mantém um engajamento alto e muita credibilidade no seu nicho. É considerado um especialista e converte muito bem (as pessoas confiam na sua indicação).
    * **Estratégia:** Firme-se como a referência da sua área. Continue próximo do público, crie conteúdo que mostre que você entende do assunto. Comece a deixar sua identidade visual mais profissional. Busque parcerias mais longas com marcas que confiam na sua expertise.

* **Médio Influenciador (100 mil a 500 mil seguidores):**
    * **Sua Força:** Seu alcance já é bem grande, seu público começa a ficar mais variado. É um momento de transição importante.
    * **Estratégia:** Profissionalize ainda mais sua produção (invista em qualidade de imagem e som). Você pode começar a explorar temas um pouco diferentes, mas cuidado para não se afastar muito do que te trouxe até aqui. Talvez seja hora de pensar em outros formatos (podcast, blog?). Manter o engajamento alto é crucial para mostrar seu valor às marcas.

* **Macro Influenciador (500 mil a 1 milhão de seguidores):**
    * **Sua Força:** Alcance enorme, você já é quase uma celebridade digital!
    * **Desafio:** O engajamento em porcentagem tende a cair, e o público é bem diverso.
    * **Estratégia:** Invista pesado em produção de altíssima qualidade. É importante continuar relevante e tentar criar formas de se conectar com o público (talvez grupos fechados, eventos?). Gerenciar sua reputação vira algo sério. Colaborar com outros criadores grandes pode ser interessante.

* **Mega Influenciador (mais de 1 milhão de seguidores):**
    * **Sua Força:** Alcance gigantesco, você influencia a cultura.
    * **Desafio:** Engajamento percentual baixo, sua imagem vai além da internet.
    * **Estratégia:** O gerenciamento da sua imagem parece quase o de uma empresa (relações públicas, gestão de crises). É preciso escolher um posicionamento muito claro (você é super exclusivo e aspiracional OU mais próximo e vulnerável?) e ser muito coerente. Cuidado com polêmicas, pois grandes marcas se afastam. Muitos nessa fase criam seus próprios negócios e marcas.

*Tendência Geral: Quanto menor, mais foco em nicho e proximidade. Quanto maior, mais profissionalismo e alcance em massa. Mas todos, em todas as fases, precisam ser autênticos e coerentes!*

**Em qual dessas fases você se vê hoje? A estratégia descrita faz sentido para sua realidade atual? Quer discutir como se preparar para a próxima fase ou como fortalecer seu posicionamento atual?**
  `;
}

/**
 * Explica como a imagem pessoal e o branding impactam a monetização (Didático e Guiado).
 */
export function explainImageAndMonetization(): string {
    return `
**Como Sua Imagem Pessoal Ajuda (ou Atrapalha) a Ganhar Dinheiro?**

Sua imagem e sua marca pessoal não são só "perfumaria", elas são ferramentas poderosas que afetam diretamente suas chances de monetizar seu trabalho. Veja como:

* **Atrair as Marcas Certas:** As marcas querem trabalhar com criadores que **combinem** com elas (seus valores, seu estilo, seu público). Se você tem um posicionamento claro (sabe qual seu nicho, quais seus valores) e uma identidade visual consistente, fica muito mais fácil para as marcas certas te encontrarem e quererem fazer parcerias.
* **Cobrar Mais Caro (Percepção de Valor):** Uma imagem profissional (conteúdo bem feito, visual caprichado) faz com que seu trabalho seja percebido como mais valioso, justificando cachês melhores. Se você é reconhecido como uma **autoridade** no seu nicho, pode cobrar ainda mais por esse conhecimento especializado. Sua autenticidade e um bom engajamento também mostram para as marcas que investir em você traz retorno, o que te dá mais poder de negociação.
* **Se Destacar na Multidão:** Ter uma marca pessoal forte te diferencia de outros milhares de criadores. Você não é só "mais um", você tem um nome, um estilo, uma reputação. Isso permite que você negocie baseado no valor único que *você* agrega, e não só no número de seguidores.
* **Abrir Novas Portas (Além da Publi):** Uma imagem bem construída pode te levar a oportunidades de monetização diferentes da publicidade tradicional:
    * **Ser Embaixador de Marca:** Parcerias mais longas e profundas.
    * **Criar Junto com Marcas (Co-criação):** Lançar produtos ou coleções em conjunto.
    * **Ter Seu Próprio Negócio:** Usar a confiança e a audiência que você construiu para lançar sua própria marca de produtos ou serviços (como muitos influenciadores fazem hoje).
* **Passar Profissionalismo:** Ter um "media kit" (sua apresentação comercial), responder rápido e de forma clara, cumprir prazos, ter um CNPJ (para quem já está maior)... tudo isso reforça que você leva seu trabalho a sério e atrai parcerias mais profissionais e maiores.
* **O Risco da Incoerência:** Fazer publicidade de algo que não tem nada a ver com você ou seus valores, ou ter uma imagem toda bagunçada e inconsistente, pode fazer seu público perder a confiança. E isso afasta tanto seguidores quanto marcas sérias.

*Conclusão: Cuidar da sua imagem pessoal de forma estratégica não é gasto, é investimento direto no seu potencial de ganhar dinheiro, hoje e no futuro.*

**Você já pensa na sua imagem como um ativo para monetização? Quer ideias de como profissionalizar sua apresentação para marcas ou como identificar parcerias que realmente combinem com você?**
  `;
}

